const mongoose = require('mongoose');

const pontoSchema = new mongoose.Schema({
  nome: {
    type: String,
    required: true,
  },
  descricao: {
    type: String,
    required: true,
  },
  geometria: {
    type: {
      type: String,
      enum: ['Point'],
      required: true,
    },
    coordinates: {
      type: [Number],
      required: true,
    },
  },
});

const Ponto = mongoose.model('Ponto', pontoSchema);

module.exports = Ponto;